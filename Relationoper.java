public class Relationoper {
    public static void main(String[] args) {
        byte value1=12,value2=5;
        System.out.println("equal or not " + (value1==value2));
        System.out.println("Greater than or not " + (value1>value2));
        System.out.println("Less than or not " + (value1<value2));
        System.out.println("Greater than or equal to " + (value1>=value2));
        System.out.println("Less than or equals to " + (value1<=value2));
    }
}
