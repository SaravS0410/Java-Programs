public class AssignmentOperator {
    public static void main(String[] args) {
        int a;
        a = 10;
        float b;
        b = 34.45f;
        double e;
        e = 65.43;
        String c;
        c = "Saravanan";

        System.out.println("Integer "+ a);
        System.out.println("Float "+ b);
        System.out.println("String "+ c);
        System.out.println("Double "+ e);
    }
}
