public class Operators1 {
   public static void main(String[] args) {
        byte var = 10;
        byte a = var;
        System.out.println("a ="+ var);
        var +=a;
        System.out.println("var +=a ="+ var);
        var *=a;
        System.out.println("var *=a ="+ var);
        var-=a;
        System.out.println("var-=a ="+ var);
   }
    
}
